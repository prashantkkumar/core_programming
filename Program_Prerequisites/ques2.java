// 2. Add Two Numbers
import java.util.*;
public class ques2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the first number = ");
        int n1 = sc.nextInt();
        System.out.println("Enter the second number = ");
        int n2 = sc.nextInt();
        int sum = n1+n2;
        System.out.println("Sum of two numbers is = "+sum);
    }
}
